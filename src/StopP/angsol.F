	PROGRAM ANGSOL
	WRITE(5,'(1X,''DIMENSIONI NEL PRIMO QUADRANTE'')')
	CALL RIN3('$ DORIZZ,DVERT,DIST:',X0,Y0,Z0)
        CALL IIN2('$ NSTEPX,NSTEPY:',NX,NY)
	RX=NX
	RY=NY
	DX=X0/RX
	DY=Y0/RY
	OMEGA=0
	DO IX=1,NX
	  DO IY=1,NY
	    OMEGA=OMEGA+Z0/(Z0**2+(DX*(FLOAT(IX)-0.5))**2+(DY*
	1        (FLOAT(IY)-0.5))**2)**1.5
	  ENDDO
	ENDDO
	OMEGA=OMEGA*DX*DY
	WRITE(5,'('' Omega ='',F10.4)')OMEGA
	END
